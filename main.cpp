#include <iostream>
using namespace std;

int main() {
    // Writing numbers from 1 to 10
    cout << "Numbers 1 to 10:" << endl;
    for (int i = 1; i <= 10; ++i) {
        cout << i << " ";
    }
    cout << endl;

    // Reversing the numbers
    cout << "Reversed numbers:" << endl;
    for (int i = 10; i >= 1; --i) {
        cout << i << " ";
    }
    cout << endl;

    return 0;
}
